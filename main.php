<h1>Cardiothoracic Surgery</h1>
<i class="fas fa-hand-holding-heart"></i>
<br>
<br>
<div class="service">
<p>
This deals with illnesses of the heart, lungs, oesophagus and chest. These include: cardiac surgery (heart and great vessels), thoracic surgery (organs within the thorax, excluding the heart), transplantation and heart failure surgery, oesophageal surgery and congenital surgery in adults and children. Procedures tend to be major and often complex.
 
Within cardiac surgery, the most common operations are coronary artery bypass grafting and valve operations. In thoracic surgery, the most common operations are lobectomy or pneumonectomy for carcinoma of the lung.
 
Working conditions
Clinical time is generally split evenly between operating, outpatient work, time spent with patients and families, and administration. A lot of time is spent in intensive care and high dependency units. Most work is elective (pre-booked, non-emergency) but there is some emergency out-of hour’s work.
 
Heart transplant surgery involves long, demanding surgery, often at night. There is a relatively low volume of patients but you will continue to see them for a long period of time. Cardiothoracic surgery involves less emergency work than general or orthopaedic surgery.
 
</p>


